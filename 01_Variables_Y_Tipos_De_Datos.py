# Variables y tipos de datos
x = 5                  # Variable entera
y = "Hola Mundo"       # Variable de texto (string)
z = 3.14159            # Variable flotante (float)

# Operaciones con variables
suma = x + z
print(f"Suma: {suma}")

# Métodos de cadenas (Strings)
cadena = "python"
print(cadena.upper())  # Convertir a mayúsculas
print(cadena.lower())  # Convertir a minúsculas
print(cadena.title())  # Convertir a formato título

# Concatenar cadenas
nombre = "Farid"
apellido = "Sánchez"
nombre_completo = nombre + " " + apellido
print(f"Nombre completo: {nombre_completo}")

# Uso de saltos de línea y tabulaciones
print("Primer línea\nSegunda línea")
print("Item1\tItem2")
