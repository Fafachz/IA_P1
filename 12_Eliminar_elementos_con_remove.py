#Elimina de la siguiente lista los elementos 'amarillo' y 'rojo'. Solo puedes eliminarlos utilizando el método remove().

colores = ['rojo', 'azul', 'verde', 'amarillo', 'marrón', 'lila', 'negro', 'rosa', 'blanco', 'naranja'] #Crea lista de colores
colores.remove('amarillo') #Elimina elemendo de la lista, en este caso color amarillo
colores.remove('rojo') #Elimina elemento de la lista, en este otro caso color rojo
print(colores) #Imprime la lista completa en la terminal